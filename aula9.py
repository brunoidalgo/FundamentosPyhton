adicao = 10 + 10
print('O resultado é:', adicao)

subtracao = 10 - 5
print('O resultado é:', subtracao)

multiplicacao = 10 * 10
print('O resultado é:', multiplicacao)

divisao = 10 / 5
print('O resultado é:', divisao)
# Essa operação sempre retornará um valor Float( Numero Decimal)

divisao = 10 // 5
print('O resultado é:', divisao)
# Essa divisão com duas // irá retornar um valor inteiro( Positivos ou negativos somente, sem decimais)

exponenciacao = 2 ** 10
print('O resultado é:', exponenciacao)

modulo = 10 % 5  # resto da divisão
print('O resultado é:', modulo)



print(16 % 2 == 0)
print(16 % 3 == 0) # Conta pra saber se um numero é par ou impar, que resultará em bool


